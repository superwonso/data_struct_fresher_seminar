#include<stdio.h>
#include<stdlib.h>
#include<string.h>

void set_string(char strings[5][100]){
    strcpy(strings[0], "\"재귀함수가 뭔가요?\"");
    strcpy(strings[1], "\"잘 들어보게. 옛날옛날 한 산 꼭대기에 이세상 모든 지식을 통달한 선인이 있었어.");
    strcpy(strings[2], "마을 사람들은 모두 그 선인에게 수많은 질문을 했고, 모두 지혜롭게 대답해 주었지.");
    strcpy(strings[3], "그의 답은 대부분 옳았다고 하네. 그런데 어느 날, 그 선인에게 한 선비가 찾아와서 물었어.\"");
    strcpy(strings[4], "라고 답변하였지.");
};

void recursion(int N, int n, char strings[5][100]){
    char ub[]="____";
    if(n==0){
        for(int k=0;k<(N-n);k++)
            printf("%s", ub);
        printf("\"재귀함수가 뭔가요?\"\n");
        for(int k=0;k<(N-n);k++)
            printf("%s", ub);
        printf("\"재귀함수는 자기 자신을 호출하는 함수라네\"\n");
        for(int k=0;k<(N-n);k++)
            printf("%s", ub);
        printf("라고 답변하였지.\n");
    }
    else{
        for(int i=0;i<5;i++){
            if(i==4) recursion(N, (n-1), strings);
            if(n!=N){
                for(int k=0;k<(N-n);k++)
                printf("%s", ub);
            }
            printf("%s\n",strings[i]);
        }
    }

};

int main(){
    int n;
    char strings[5][100];
    set_string(strings);

    scanf("%d", &n);
    printf("어느 한 컴퓨터공학과 학생이 유명한 교수님을 찾아가 물었다.\n");
    recursion(n, n, strings);
    return 0;
}